
export function returnFriendsModalTitle (shareModalStep) {
  if (shareModalStep === 'ballotShareOptions') {
    return 'Share Ballot';
  } else if (shareModalStep === 'ballotShareOptionsAllOpinions') {
    return 'Share Ballot';
  } else if (shareModalStep === 'candidateShareOptions') {
    return 'Share Candidate';
  } else if (shareModalStep === 'candidateShareOptionsAllOpinions') {
    return 'Share Candidate';
  } else if (shareModalStep === 'measureShareOptions') {
    return 'Share Measure';
  } else if (shareModalStep === 'measureShareOptionsAllOpinions') {
    return 'Share Measure';
  } else if (shareModalStep === 'officeShareOptions') {
    return 'Share Office';
  } else if (shareModalStep === 'officeShareOptionsAllOpinions') {
    return 'Share Office';
  } else if (shareModalStep === 'organizationShareOptions') {
    return 'Share This Page';
  } else if (shareModalStep === 'organizationShareOptionsAllOpinions') {
    return 'Share This Page';
  } else if (shareModalStep === 'readyShareOptions') {
    return 'Share This Page';
  } else if (shareModalStep === 'readyShareOptionsAllOpinions') {
    return 'Share This Page';
  }
  return 'Share';
}

